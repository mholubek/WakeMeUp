export const resolveLanguage = (rawLanguage?: string): string => {
  if (!rawLanguage) {
    return "en";
  }

  const normalized = rawLanguage.toLowerCase();
  if (normalized.startsWith("tlh")) {
    return "tlh";
  }

  const base = normalized.split(/[-_]/)[0];
  return baseOverrides[base] ? base : "en";
};

export const getTranslations = (rawLanguage?: string): TranslationSet => {
  const language = resolveLanguage(rawLanguage);
  return {
    ...english,
    ...(baseOverrides[language] ?? {}),
  };
};
